// Unicode bold character converter
// Converts regular text to Unicode Mathematical Bold characters

const boldMap = {
    // Uppercase letters - Sans-serif bold
    'A': 'ð—”', 'B': 'ð—•', 'C': 'ð—–', 'D': 'ð——', 'E': 'ð—˜', 'F': 'ð—™', 'G': 'ð—š', 'H': 'ð—›', 'I': 'ð—œ', 'J': 'ð—',
    'K': 'ð—ž', 'L': 'ð—Ÿ', 'M': 'ð— ', 'N': 'ð—¡', 'O': 'ð—¢', 'P': 'ð—£', 'Q': 'ð—¤', 'R': 'ð—¥', 'S': 'ð—¦', 'T': 'ð—§',
    'U': 'ð—¨', 'V': 'ð—©', 'W': 'ð—ª', 'X': 'ð—«', 'Y': 'ð—¬', 'Z': 'ð—­',
    
    // Lowercase letters - Sans-serif bold
    'a': 'ð—®', 'b': 'ð—¯', 'c': 'ð—°', 'd': 'ð—±', 'e': 'ð—²', 'f': 'ð—³', 'g': 'ð—´', 'h': 'ð—µ', 'i': 'ð—¶', 'j': 'ð—·',
    'k': 'ð—¸', 'l': 'ð—¹', 'm': 'ð—º', 'n': 'ð—»', 'o': 'ð—¼', 'p': 'ð—½', 'q': 'ð—¾', 'r': 'ð—¿', 's': 'ð˜€', 't': 'ð˜',
    'u': 'ð˜‚', 'v': 'ð˜ƒ', 'w': 'ð˜„', 'x': 'ð˜…', 'y': 'ð˜†', 'z': 'ð˜‡',
    
    // Numbers - Sans-serif bold
    '0': 'ðŸ¬', '1': 'ðŸ­', '2': 'ðŸ®', '3': 'ðŸ¯', '4': 'ðŸ°', '5': 'ðŸ±', '6': 'ðŸ²', '7': 'ðŸ³', '8': 'ðŸ´', '9': 'ðŸµ'
};

/**
 * Convert text to Unicode bold characters
 * @param {string} text - Text to convert
 * @returns {string} - Text with Unicode bold characters
 */
export function toBold(text) {
    if (!text) return '';
    
    return text.split('').map(char => boldMap[char] || char).join('');
}

/**
 * Convert text to bold, but preserve certain characters normal (like emojis, punctuation)
 * @param {string} text - Text to convert
 * @returns {string} - Text with Unicode bold characters
 */
export function toBoldPreserve(text) {
    return toBold(text);
}
